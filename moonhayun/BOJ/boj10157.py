"""
[문제]
어떤 공연장에는 가로로 C개, 세로로 R개의 좌석이 C×R격자형으로 배치되어 있다. 
각 좌석의 번호는 해당 격자의 좌표 (x,y)로 표시된다. 

이 공연장에 입장하기 위하여 많은 사람이 대기줄에 서있다. 
기다리고 있는 사람들은 제일 앞에서부터 1, 2, 3, 4, . 순으로 대기번호표를 받았다. 
우리는 대기번호를 가진 사람들에 대하여 (1,1)위치 좌석부터 시작하여 시계방향으로 돌아가면서 
비어있는 좌석에 관객을 순서대로 배정한다. 이것을 좀 더 구체적으로 설명하면 다음과 같다.

먼저 첫 번째 사람, 즉 대기번호 1인 사람은 자리 (1,1)에 배정한다. 
그 다음에는 위쪽 방향의 좌석으로 올라가면서 다음 사람들을 배정한다. 
만일 더 이상 위쪽 방향으로 빈 좌석이 없으면 오른쪽으로 가면서 배정한다. 
오른쪽에 더 이상 빈자리가 없으면 아래쪽으로 내려간다. 
그리고 아래쪽에 더 이상 자리가 없으면 왼쪽으로 가면서 남은 빈 좌석을 배정한다. 
이 후 왼쪽으로 더 이상의 빈 좌석이 없으면 다시 위쪽으로 배정하고, 
이 과정을 모든 좌석이 배정될 때까지 반복한다. 

아래 그림은 7×6공연장에서 대기번호 1번부터 42번까지의 관객이 좌석에 배정된 결과를 보여주고 있다.

여러분은 공연장의 크기를 나타내는 자연수 C와 R이 주어져 있을 때, 대기 순서가 K인 관객에게 배정될 좌석 번호 (x,y)를 찾는 프로그램을 작성해야 한다. 

[입력]
첫 줄에는 공연장의 격자 크기를 나타내는 정수 C와 R이 하나의 공백을 사이에 두고 
차례대로 주어진다. 두 값의 범위는 5 ≤ C, R ≤ 1,000이다. 
그 다음 줄에는 어떤 관객의 대기번호 K가 주어진다. 단 1 ≤ K ≤ 100,000,000이다.

[출력]
입력으로 제시된 대기번호 K인 관객에게 배정될 좌석번호 (x,y)를 구해서 
두 값, x와 y를 하나의 공백을 사이에 두고 출력해야 한다. 
만일 모든 좌석이 배정되어 해당 대기번호의 관객에게 좌석을 배정할 수 없는 경우에는 0(숫자 영)을 출력해야 한다. 
"""
import sys
input = sys.stdin.readline

C, R = map(int, input().split())
K = int(input())

# 좌석 수보다 K가 크면 배정 불가
if K > C * R:
    print(0)
else:
    visited = [[False] * C for _ in range(R)]

    dirs = [(-1, 0), (0, 1), (1, 0), (0, -1)]
    d = 0

    # 시작 좌표
    r, c = R - 1, 0
    visited[r][c] = True
    cnt = 1

    while cnt < K:
        nr = r + dirs[d][0]
        nc = c + dirs[d][1]

        # 막히면 방향 전환
        if not (0 <= nr < R and 0 <= nc < C) or visited[nr][nc]:
            d = (d + 1) % 4
            nr = r + dirs[d][0]
            nc = c + dirs[d][1]

        r, c = nr, nc
        visited[r][c] = True
        cnt += 1

    print(c + 1, R - r)