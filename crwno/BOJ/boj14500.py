N, M = map(int, input().split())
board = [list(map(int, input().split())) for _ in range(N)]

mx_s = 0
s = 0
shape = [[(0, 0), (0, 1), (1, 0), (1, 1)], #정사각형
         [(0, 0), (0, 1), (0, 2), (0, 3)],
         [(0, 0), (1, 0), (2, 0), (3, 0)], #직선
         [(0, 0), (0, -1), (0, 1), (1, 0)],
         [(-1, 0), (0, 0), (0, 1), (1, 0)],
         [(-1, 0), (0, -1), (0, 0), (1, 0)],
         [(-1, 0), (0, -1), (0, 1), (0, 0)], # ㅗ
         [(0, 0), (1, 0), (2, 0), (2, 1)],
         [(0, 0), (1, 0), (2, 0), (2, -1)],
         [(0, 0), (1, 0), (1, 1), (1, 2)],
         [(0, 0), (1, 0), (1, -1), (1, -2)],
         [(0, 0), (-1, 0), (-2, 0), (-2, 1)],
         [(0, 0), (-1, 0), (-2, 0), (-2, -1)],
         [(0, 0), (-1, 0), (-1, 1), (-1, 2)],
         [(0, 0), (-1, 0), (-1, -1), (-1, -2)], # ㄱㄴ
         [(0, 0), (1, 0), (1, 1), (2, 1)],
         [(0, 0), (1, 0), (1, -1), (2, -1)],
         [(0, 0), (0, 1), (1, 1), (1, 2)],
         [(0, 0), (0, -1), (1, -1), (1, -2)]] # Z
for i in range(N):
    for j in range(M):
        for item in shape:
            s = 0
            for di, dj in item:
                ni, nj = i + di, j + dj
                if 0 <= ni < N and 0 <= nj < M:
                    s += board[ni][nj]
                else:
                    break

                if mx_s < s:
                    mx_s = s
print(mx_s)